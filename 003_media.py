#Escreva um programa aonde o usuário digite duas notas e ele mostre a média das duas notas

nota1 = float(input('digite a primeira nota: '))
nota2 = float(input('digite a segunda nota: '))

media = (nota1 + nota2) / 2

print(f'A primeira nota do aluno é {nota1}, a segunda nota é {nota2}, e a média do aluno é {media}')
